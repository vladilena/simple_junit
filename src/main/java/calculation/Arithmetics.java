package calculation;

public class Arithmetics {
    public double add(double a, double b) {
        return a + b;
    }

    public double dedut(double a, double b) {
        return a - b;
    }

    public double mult(double a, double b) {
        return a * b;
    }

    public double div(double a, double b) {
        return a / b;
    }

}